package io.trewartha.positional.settings

/**
 * Themes for the application to use
 */
enum class Theme {

    /**
     * Device's default theme (i.e. if the device is in dark mode, use dark mode)
     */
    DEVICE,

    /**
     * Light theme
     */
    LIGHT,

    /**
     * Dark theme
     */
    DARK
}
